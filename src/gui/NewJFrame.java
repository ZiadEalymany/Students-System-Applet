    private void update_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_saveActionPerformed
        // TODO add your handling code here:
        try {
            mainApp m = new mainApp();
            session = factory.getCurrentSession();
            session.beginTransaction();
            String op;
            if (update_dept.getSelectedIndex() != 0) {
                boolean u = m.updateStudent(session, update_id.getText(), update_name.getText(), (String) update_dept.getSelectedItem(), update_phn.getText());
                if (u) {
                    op = "Student Updated Successfully!!";
                    System.out.println(op);
                    JOptionPane.showMessageDialog(null, op);
                    update_cancelActionPerformed(evt);
                    clearActionPerformed(evt);
                } else {
                    op = "The Student does not exist!";
                    System.out.println(op);
                    JOptionPane.showMessageDialog(null, op);
                }
            } else {
                op = "choose a department!";
                System.out.println(op);
                JOptionPane.showMessageDialog(null, op);
            }
            session.close();
        } catch (HibernateException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_update_saveActionPerformed

