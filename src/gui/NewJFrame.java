    public void clicked() {
        jTable1.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(final MouseEvent e) {
                if (e.getClickCount() == 1) {
                    final JTable jTable = (JTable) e.getSource();
                    final int row = jTable.getSelectedRow();
                    //final int column = jTable.getSelectedColumn();
                    final String IdCell = (String) jTable.getValueAt(row, 0);
                    final String nameCell = (String) jTable.getValueAt(row, 1);
                    final String deptCell = (String) jTable.getValueAt(row, 2);
                    final String phoneCell = (String) jTable.getValueAt(row, 3);
                    update_id.setText(IdCell);
                    update_name.setText(nameCell);
                    update_dept.setSelectedItem(deptCell);
                    update_phn.setText(phoneCell);
                }
            }
        }
        );
    }

    public void displayTable() {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/student_task?useSSL=false",
                    "hbstudent", "hbstudent");
            String sql = "SELECT * FROM student_task.student";
            PreparedStatement prst = conn.prepareStatement(sql);
            ResultSet rs = prst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));

            clicked();
        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    public void searchTable(String name) {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/student_task?useSSL=false",
                    "hbstudent", "hbstudent");
            String query = "SELECT * FROM Student where name like '%" + name + "%'" + " or id like '%" + name + "%'";
            PreparedStatement prst = conn.prepareStatement(query);
            ResultSet rs = prst.executeQuery();
            jTable1.setModel(DbUtils.resultSetToTableModel(rs));

            update_id.setText("");
            update_name.setText("");
            update_dept.setSelectedIndex(0);
            update_phn.setText("");
            clicked();
            if (jTable1.getRowCount() == 1) {
                update_id.setText((String) jTable1.getValueAt(0, 0));
                update_name.setText((String) jTable1.getValueAt(0, 1));
                update_dept.setSelectedItem((String) jTable1.getValueAt(0, 2));
                update_phn.setText((String) jTable1.getValueAt(0, 3));
            }

        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

