    private void add_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_saveActionPerformed
        // TODO add your handling code here:
        try {
            String pr;
            mainApp m = new mainApp();
            session = factory.getCurrentSession();
            session.beginTransaction();
            if (add_id.getText().length() == 14) {
                if (!add_name.getText().equals("")) {
                    if (add_dept.getSelectedIndex() != 0) {
                        if (add_phn.getText().length() == 11 && add_phn.getText().startsWith("01")) {
                            boolean c = m.createStudent(session, add_id.getText(), add_name.getText(), (String) add_dept.getSelectedItem(), add_phn.getText());
                            if (c) {
                                pr = "New Student has been saved";
                                System.out.println(pr);
                                JOptionPane.showMessageDialog(null, pr);

                                add_cancelActionPerformed(evt);
                                jFrame1.setVisible(true);
                                clearActionPerformed(evt);
                            } else {
                                pr = "You can not add a new Student because there is an existing one with this Id: " + add_id.getText();
                                System.out.println(pr);
                                JOptionPane.showMessageDialog(null, pr);
                            }
                        } else {
                            pr = "Phone number should be of 11 digits and starts with '01' ";
                            System.out.println(pr);
                            JOptionPane.showMessageDialog(null, pr);
                        }
                    } else {
                        pr = "choose a department!";
                        System.out.println(pr);
                        JOptionPane.showMessageDialog(null, pr);
                    }
                } else {
                    pr = "Student name can not be empty";
                    System.out.println(pr);
                    JOptionPane.showMessageDialog(null, pr);
                }

            } else {
                pr = "ID should be of 14 digits";
                System.out.println(pr);
                JOptionPane.showMessageDialog(null, pr);
            }

            session.close();
        } catch (HibernateException e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_add_saveActionPerformed

